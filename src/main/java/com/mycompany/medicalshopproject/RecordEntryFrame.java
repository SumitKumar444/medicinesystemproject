    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name=(String)jTextField1.getText();
        String rate=(String)jTextField2.getText();
        String Qty= (String)jTextField3.getText();
        int m=Integer.parseInt(Qty);
        int n=Integer.parseInt(rate);
        if(name.equals("")||rate.equals("")||Qty.equals(""))
        JOptionPane.showMessageDialog(this, "please enter medicine, rate and Quantity");
        else
        {
        try
        {
            if(m<=0||n<=0)
                JOptionPane.showMessageDialog(this, "please enetr some valid Quantity and rate");
            else
            {
            Connection conn=new GetConnection().getconnection();
            PreparedStatement ps=conn.prepareStatement("insert into medicine values(?,?,?)");
            ps.setString(1,name);
            ps.setInt(2, Integer.parseInt(rate));
            ps.setInt(3, Integer.parseInt(Qty));
            int x=ps.executeUpdate();
            if(x==1)
                JOptionPane.showMessageDialog(this,"record insert successfully");
            else
                JOptionPane.showMessageDialog(this,"record not insert");
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


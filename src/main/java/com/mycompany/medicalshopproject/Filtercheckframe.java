    public void getstock(int n,String s)
    {
        jPanel6.setVisible(true);
        Connection conn;
        try
        {
            conn=new GetConnection().getconnection();
            if(n==1)
            {
                PreparedStatement ps=conn.prepareStatement("select mname,available from medicine where mname=?");
                ps.setString(1,s);
                ResultSet rs=ps.executeQuery();
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else if(n==2)
            {
                String symbol=s.substring(0,1);
                int c=Integer.parseInt(s.substring(1));
                PreparedStatement ps=conn.prepareStatement("select mname,available from medicine where available "+symbol+"?");
                ps.setInt(1, c);
                ResultSet rs=ps.executeQuery();
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else
            {
                PreparedStatement ps=conn.prepareStatement("select mname,available from medicine where mname like ?");
                ps.setString(1, s+"%");
                ResultSet rs=ps.executeQuery();
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }


